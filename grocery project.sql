-- 1️. Customer Insights

-- ● How many unique customers have placed orders?
select count(CustomerID) from customers;       



-- ● Which customers have placed the highest number of orders?
select * from orderdetails   
order by Quantity desc;
select c.name,od.Quantity from customers c
join orders o
on c.CustomerID = o.CustomerID
join orderdetails od
on o.OrderID = od.OrderID
having Quantity = 5;
-- ● What is the total and average purchase value per customer?
select c.CustomerID,c.Name,sum(od.TotalPrice) as Total, avg(od.TotalPrice) as Average from customers c   
join orders o 
on c.CustomerID = o.CustomerID
join orderdetails od
on o.OrderID = od.OrderID
group by c.CustomerID,c.Name
order by customerID asc;
-- ● Who are the top 5 customers by total purchase amount?        
select c.Name,sum(od.TotalPrice) as TotalPurchase from customers c
join orders o
on c.CustomerID = o.CustomerID
join orderdetails od
on o.OrderID = od.OrderID
group by c.Name
order by TotalPurchase desc
limit 5;

-- 2. Product Performance
-- Evaluate how well products are performing in terms of sales and revenue.
-- ● How many products exist in each category?               
select c.CategoryID,c.CategoryName,count(productID) as product_count from categories c
join products p
on c.CategoryID = p.CategoryID
group by c.CategoryID,c.CategoryName;

-- ● What is the average price of products by category?
select c.CategoryID,CategoryName,avg(price) as Average from categories c
join products p
on c.CategoryID=p.CategoryID
group by c.CategoryID,CategoryName;
-- ● Which products have the highest total sales volume (by quantity)?
select p.productID,p.Name,sum(od.Quantity) from products p
join orderdetails od
on p.ProductID = od.ProductID
group by p.productID,p.Name
limit 1;
-- ● What is the total revenue generated by each product?   ##1
select p.ProductID,p.Name,sum(od.TotalPrice) from products p
join orderdetails od 
on p.ProductID = od.ProductID
group by p.ProductID,p.Name
order by productID asc;



-- ● How do product sales vary by category and supplier?
select c.CategoryID,c.CategoryName,s.SupplierID,s.SupplierName, 
sum(od.TotalPrice) as Total from orderdetails od
join products p
on od.ProductID = p.ProductID
join categories c
on p.CategoryID = c.CategoryID
join suppliers s
on s.supplierID = p.supplierID
group by  c.CategoryID,c.CategoryName,s.SupplierID,s.SupplierName
order by Total desc;
