-- 5️. Employee Performance
-- Assess how employees are handling and influencing sales.
-- ● How many employees have processed orders?
select count(distinct EmployeeID) as Total_Employees from orders;  


-- ● Which employees have handled the most orders?
select se.name,count(o.orderID) as total_order from store_employees se   
join orders o
on o.EmployeeID = se.EmployeeID
group by se.name
order by total_order desc
limit 1;

-- ● What is the total sales value processed by each employee?
select se.Name, sum(od.TotalPrice) as total_price from store_employees se
join orders o
on se.EmployeeID = o.EmployeeID
join orderdetails od
on o.OrderID = od.OrderID
group by se.Name;
-- ● What is the average order value handled per employee?         
select se.name, avg(od.TotalPrice) as order_value from store_employees se
join orders o
on se.EmployeeID = o.EmployeeID
join orderdetails od 
on o.OrderID = od.OrderID
group by se.name;


